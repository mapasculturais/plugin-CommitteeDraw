        <h1>Como o Sorteio de Pareceristas é Transparente e Auditável</h1>
    </div>

    <div class="committee-draws-process">

        <h2>Entendendo o Sorteio como uma Receita Precis</h2>
        <p>Imagine que nosso sistema de sorteio funciona como uma receita de bolo muito detalhada. Se você usar<strong>exatamente os mesmos ingredientes</strong> e seguir<strong>exatamente os mesmos passos,</strong> o resultado final será sempre o mesmo. É esse princípio que garante que nosso sorteio seja completamente transparente e verificável.</p>
    </div>

    <div class="commmitee-draws-ingredients">
        <h2>Os "Ingredientes" do Nosso Sorteio</h2>
        <p>Para realizar o sorteio, o sistema utiliza cinco informações fundamentais que funcionam como os "ingredientes" da nossa receita:</p>

        <ol>
            <li><strong>Identificação da fase do edital:</strong> O número único que identifica esta etapa específica do processo (ID: 775)</li>
            <li><strong>Comissão responsável:</strong> O nome do grupo que está realizando a avaliação ("@tiebreaker")</li>
            <li><strong>Número do sorteio:</strong> A sequência deste sorteio dentro da comissão (2º sorteio)</li>
            <li><strong>Data e hora exata:</strong> O momento preciso em que o sorteio foi realizado (2025-09-24 21:25:45)</li>
            <li><strong>Lista completa de participantes:</strong> Todos os avaliadores habilitados para este sorteio</li>
        </ol>

    </div>

    <div class="committee-draws-process">
        <h2>Como o Processo Funciona na Prática</h2>
    </div>

    <div class="committee-draws-infomation">
        <h2>Passo 1: Criação do Código Único</h2>
        <p>O sistema combina todas essas informações em um <strong>código único</strong> (chamado de seed), que funciona como uma "impressão digital" do sorteio. Este código garante que cada combinação de informações resulte em um sorteio diferente.</p>
    </div>
    <div class="committee-draws-infomation">
        <h2>Passo 2: Embaralhamento Justo</h2>
        <p>Usando esse código único, o sistema organiza a lista de avaliadores de maneira aleatória, mas completamente reproduzível. É como embaralhar cartas seguindo uma sequência específica - sempre que você repetir a mesma sequência, as cartas ficarão na mesma ordem.</p>
    </div>
    <div class="committee-draws-infomation">
        <h2>Passo 3: Seleção dos Sorteados</h2>
        <p>A partir da lista embaralhada, o sistema seleciona a quantidade necessária de avaliadores (neste caso, 5 pareceristas).</p>
    </div>
    <div class="committee-draws-infomation">
        <h2>Por Que Você Pode Confiar Neste Processo</h2>
        <h2>Total Transparência</h2>

        <p>Todas as informações utilizadas no sorteio ficam registradas e disponíveis para consulta. Não há "segredos" ou etapas ocultas.</p>

        <h2>Resultado Verificável</h2>

        <p>Qualquer pessoa pode repetir o processo usando as mesmas informações e obterá<strong>exatamente o mesmo resultado.</strong> Se você fizer a verificação e o resultado for diferente, isso significa que alguma informação não está correta.</p>

        <h2>Imparcialidade Garantida</h2>

        <p>O sistema não tem preferências ou viés. Ele trata todos os participantes de forma igualitária, seguindo apenas as regras matemáticas estabelecidas.</p>
